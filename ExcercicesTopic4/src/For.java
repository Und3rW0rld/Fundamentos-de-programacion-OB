public class For {

    /*Para el bucle For, crea una variable
    numeroFor, esta variable tendrá como valor 0 y su
    condición será que la variable sea igual o menor que 3,
    se irá incrementando en 1 su valor cada vez que se
    ejecute y deberá mostrarse por pantalla.*/

    public static void main(String[] args) {
        for(int numeroFor = 0; numeroFor <= 3; numeroFor++) {
            System.out.println(numeroFor);
        }
    }
}
