public class DoWhile {
    /*
    * Para el bucle Do While, deber√°s crear
    * la misma estructura que en el While, pero solo se debe ejecutar una vez.*/
    public static void main(String[] args) {
        int numeroWhile = 3;
        do {
            System.out.println(numeroWhile);
            numeroWhile++;
        }while (numeroWhile < 3);
    }
}
